    private void detailBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detailBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = airplaneTable.getSelectedRow();
        if (selectedRow >= 0){
            Airplane ap = (Airplane)airplaneTable.getValueAt(selectedRow,0);
            departureHourTextField.setText(String.valueOf(ap.getDepartureHour()));
            departureMinTextField.setText(String.valueOf(ap.getDepartureMin()));
            companyTextField.setText(ap.getCompany());
            productYearTextField.setText(String.valueOf(ap.getProductYear()));
            seatsNumTextField.setText(String.valueOf(ap.getSeatsNum()));
            serialNumTextField.setText(ap.getSerialNum());
            modelNumTextField.setText(ap.getModelNum());
            certYearTextField.setText(String.valueOf(ap.getCertYear()));  
            certMonTextField.setText(String.valueOf(ap.getCertMon()));
        }else
            JOptionPane.showMessageDialog(null, "Please select any row");
    }//GEN-LAST:event_detailBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = airplaneTable.getSelectedRow();
        if (selectedRow >= 0){
            Airplane ap = (Airplane)airplaneTable.getValueAt(selectedRow,0);
            al.deleteAirplane(ap);
            JOptionPane.showMessageDialog(null, "Deleted!");
            al.setUpdate();
            populateTable();
        }else
            JOptionPane.showMessageDialog(null, "Please select any row");
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void changeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeBtnActionPerformed
        // TODO add your handling code here:
        if(departureHourTextField.getText().isEmpty()|departureMinTextField.getText().isEmpty()|companyTextField.getText().isEmpty()
                |productYearTextField.getText().isEmpty()|seatsNumTextField.getText().isEmpty()|
                serialNumTextField.getText().isEmpty()|modelNumTextField.getText().isEmpty()|certYearTextField.getText().isEmpty()
                |certMonTextField.getText().isEmpty()
                ){
            JOptionPane.showMessageDialog(null, "Please enter complete the form!");
        }else if(Integer.parseInt(departureHourTextField.getText())>24|Integer.parseInt(departureMinTextField.getText())>60
                |Integer.parseInt(productYearTextField.getText())>2017|Integer.parseInt(certYearTextField.getText())>99
                |Integer.parseInt(certMonTextField.getText())>12){
            JOptionPane.showMessageDialog(null, "Please enter the correct info!");
        }else{
        int selectedRow = airplaneTable.getSelectedRow();
        if (selectedRow >= 0){
            Airplane ap = (Airplane)airplaneTable.getValueAt(selectedRow,0);
            
        int departureHour = Integer.parseInt(departureHourTextField.getText());
        int departureMin = Integer.parseInt(departureMinTextField.getText());
        String company = companyTextField.getText();
        int productYear = Integer.parseInt(productYearTextField.getText());
        int seatsNum = Integer.parseInt(seatsNumTextField.getText());
        String serialNum = serialNumTextField.getText();
        String modelNum = modelNumTextField.getText();
        int certYear = Integer.parseInt(certYearTextField.getText());
        int certMon = Integer.parseInt(certMonTextField.getText());
        
        ap.setCertYear(certYear);
        ap.setCertMon(certMon);
        ap.setCompany(company);
        ap.setDepartureHour(departureHour);
        ap.setDepartureMin(departureMin);
        ap.setModelNum(modelNum);
        ap.setProductYear(productYear);
        ap.setSeatsNum(seatsNum);
        ap.setSerialNum(serialNum);
        al.setUpdate();
        JOptionPane.showMessageDialog(null, "Updated!");
        
        departureHourTextField.setText("");
        departureMinTextField.setText("");
        companyTextField.setText("");
        productYearTextField.setText("");
        seatsNumTextField.setText("");
        serialNumTextField.setText("");
        modelNumTextField.setText("");
        certYearTextField.setText("");
        certMonTextField.setText("");
        
        populateTable();
        
       
        }else
            JOptionPane.showMessageDialog(null, "Please select any row");
        }
    }//GEN-LAST:event_changeBtnActionPerformed

