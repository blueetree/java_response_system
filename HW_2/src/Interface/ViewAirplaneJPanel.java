    public void populateTable(){
        DefaultTableModel dtm =(DefaultTableModel)airplaneTable.getModel();
        dtm.setRowCount(0);
        for(Airplane ap : al.getAirline()){
            Object row[] = new Object[7];
            row[0] = ap;
            row[1] = ap.getCompany();
            row[2] = ap.getModelNum();
            row[3] = ap.getSeatsNum();
            row[4] = (int)ap.getDepartureHour()+":"+ap.getDepartureMin();
            row[5] = (int)ap.getCertYear()+"/"+ap.getCertMon();
            row[6] = ap.getProductYear();
            dtm.addRow(row);
        }
    }
    
    public void addToTable(Airplane ad){
            DefaultTableModel dtm =(DefaultTableModel)airplaneTable.getModel();
            Object row[] = new Object[7];
            row[0] = ad;
            row[1] = ad.getCompany();
            row[2] = ad.getModelNum();
            row[3] = ad.getSeatsNum();
            row[4] = (int)ad.getDepartureHour()+":"+ad.getDepartureMin();
            row[5] = (int)ad.getCertYear()+"/"+ad.getCertMon();
            row[6] = ad.getProductYear();
            dtm.addRow(row);
    }
    
    public void saveResult(Airplane result){
        Airplane sr = searchResult.addAirplane();
        sr.setCertYear(result.getCertYear());
        sr.setCertMon(result.getCertMon());
        sr.setCompany(result.getCompany());
        sr.setDepartureHour(result.getDepartureHour());
        sr.setDepartureMin(result.getDepartureMin());
        sr.setModelNum(result.getModelNum());
        sr.setProductYear(result.getProductYear());
        sr.setSeatsNum(result.getSeatsNum());
        sr.setSerialNum(result.getSerialNum());
        searchResult.setUpdate();
    }

