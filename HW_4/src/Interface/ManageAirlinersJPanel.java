    private void btnViewAirlinerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewAirlinerActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblManageAirliners.getSelectedRow();
         if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select!");
  
         }else{
             Airliner airliner = (Airliner)tblManageAirliners.getValueAt(selectedRow,0);
             ViewAirlinerJPanel panel = new ViewAirlinerJPanel(rightJPanel,airliner);
             rightJPanel.add("ViewAirlinerJPanel",panel);
             CardLayout layout = (CardLayout)rightJPanel.getLayout();
             layout.next(rightJPanel);
         }
    }//GEN-LAST:event_btnViewAirlinerActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblManageAirliners.getSelectedRow();
        if(selectedRow >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null,"Would you like to delete the Airliner?","Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_NO_OPTION){
                Airliner airliner = (Airliner)tblManageAirliners.getValueAt(selectedRow, 0);
                airlinerDirectory.deleteAccount(airliner);
                populateTable();
            }
        }else{
                    JOptionPane.showMessageDialog(null, "Please select!");

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

