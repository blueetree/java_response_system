    private void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblManageAirliners.getModel();
        dtm.setRowCount(0);
        for(Airliner airliner: airlinerDirectory.getAirlinerDirectory()){
            Object[] row = new Object[4];
            row[0] = airliner;
            row[1] = airliner.getAirlineName();
            row[2] = airliner.getTotalFlightPerDay();
            row[3] = airliner.getFlightSchedule();
            
            dtm.addRow(row);
        }
    }

