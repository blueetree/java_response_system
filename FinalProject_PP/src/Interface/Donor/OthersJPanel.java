    public OthersJPanel(JPanel donorContainer, EcoSystem system) {
        initComponents();
        this.donorContainer = donorContainer;
        this.system = system;
        populateDonateInformationTable();

        for (UserAccount account : system.getUserAccountDirectory().getUserAccountList()) {
            System.out.println(account.getRole().getClass().getTypeName());
            if (account.getRole().getClass().getTypeName().equals("Business.Role.DonorRole")) {
                DonorAccount donorAccount = (DonorAccount) account;
                for (WorkRequest workRequest : system.getWorkQueue().getWorkRequestList()) {
                    if (workRequest.getSender().equals(account)) {
                        if (donorAccount.getTotalAmount() == 0.0) {
                            if (workRequest.getMoney() != null) {
                                donorAccount.setTotalAmount(donorAccount.getTotalAmount() + workRequest.getMoney());
                            } else {
                                donorAccount.setTotalAmount(donorAccount.getTotalAmount() + (workRequest.getItem().getQuantity() * workRequest.getItem().getPrice()));
                            }
                        }else if(donorAccount.getTotalAmount() != 0.0){
                        if (workRequest.getMoney() != null) {
                                donorAccount.setTotalAmount(donorAccount.getTotalAmount() + workRequest.getMoney());
                            } else {
                                donorAccount.setTotalAmount(donorAccount.getTotalAmount() + (workRequest.getItem().getQuantity() * workRequest.getItem().getPrice()));
                            }
                        }
                    }
                }
            }
        }

        donorAccountList = new ArrayList<DonorAccount>();

        for (UserAccount account : system.getUserAccountDirectory().getUserAccountList()) {
            if (account.getRole().getClass().getTypeName().equals("Business.Role.DonorRole")) {
                DonorAccount donorAccount = (DonorAccount) account;
                donorAccountList.add(donorAccount);
            }
        }

        Collections.sort(donorAccountList);
        //To change body of generated methods, choose Tools | Templates.
    }

