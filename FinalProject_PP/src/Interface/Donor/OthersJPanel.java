    public void populateTop() {
        int rc = tblTop.getModel().getRowCount() - 1;
        while (rc >= 0) {
            ((DefaultTableModel) tblTop.getModel()).removeRow(rc);
            rc = rc - 1;
        }
        int top = 0;
        for (DonorAccount donorAccount : donorAccountList) {
            Object[] row = new Object[3];

            row[0] = ++top;
            row[1] = donorAccount.getUsername();
            row[2] = donorAccount.getTotalAmount();
            ((DefaultTableModel) tblTop.getModel()).addRow(row);
        }
    }
