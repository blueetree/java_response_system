    private void btnApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyActionPerformed
        
        donorAccount.setApply(true);
        donorAccount.setApplyUserName(txtUserName.getText());
        donorAccount.setApplyPassword(txtPassword.getText());
        if (cbxType.getSelectedItem().equals("Volunteer at front line")) {
            donorAccount.setApplyWork("FrontLine");
        } else if (cbxType.getSelectedItem().equals("Procurement staff")) {
            donorAccount.setApplyWork("Purchase");
        } else if (cbxType.getSelectedItem().equals("Commanding officer")) {
            donorAccount.setApplyWork("Command");
        } 
        JOptionPane.showMessageDialog(null, "Apply Successfully!", "Successfully", JOptionPane.INFORMATION_MESSAGE); 
        // TODO add your handling code here:
    }//GEN-LAST:event_btnApplyActionPerformed

