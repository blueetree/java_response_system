    public void populateCbxCountry() {
        cbxCountry.removeAllItems();
        for (Country country : system.getCountryList()) {
            cbxCountry.addItem(country);
        }
    }

    public void populateCbxDistrict(Country country) {
        cbxDistrict.removeAllItems();
        for (District district : country.getDistrictDirectory().getDistrictList()) {
            cbxDistrict.addItem(district);
        }
    }

    public void populateTxtNeedingQuantity() {

        if (cbxType.getSelectedItem().equals("Volunteer at front line")) {
            txtNeedingQuantity.setText((String.valueOf(frontLineQuantity)));
        } else if (cbxType.getSelectedItem().equals("Procurement staff")) {
            txtNeedingQuantity.setText((String.valueOf(purchaseQuantity)));
        } else if (cbxType.getSelectedItem().equals("Commanding officer")) {
            txtNeedingQuantity.setText((String.valueOf(commandQuantity)));

        }
    }

