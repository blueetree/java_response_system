    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        donorAccount.setUsername(txtUserName.getText());
        donorAccount.setPassword(txtPassword.getText());
        donorAccount.setAge(Integer.parseInt(txtAge.getText()));
        donorAccount.setOccupation(txtOccupation.getText());
        if (cbxType.getSelectedItem().equals("Group")) {
            donorAccount.setType(false);
        } else if (cbxType.getSelectedItem().equals("Individual")) {
            donorAccount.setType(true);
        }
        JOptionPane.showMessageDialog(null, "Update Successfully!", "Successfully", JOptionPane.INFORMATION_MESSAGE);
        // TODO add your handling code here:
    }//GEN-LAST:event_btnSaveActionPerformed

    private void cbxTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxTypeActionPerformed
        if (cbxType.getSelectedItem().equals("Group")) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int DialogResult = JOptionPane.showConfirmDialog(null, "Group Can Not Apply For Volunteer", "Warning", dialogButton);
            if (DialogResult == JOptionPane.YES_OPTION) {
                donorAccount.setType(true);
            } else if (DialogResult == JOptionPane.NO_OPTION) {
                donorAccount.setType(false);
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_cbxTypeActionPerformed


