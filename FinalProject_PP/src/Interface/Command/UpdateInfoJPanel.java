    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblArea.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row first", "Warning", JOptionPane.WARNING_MESSAGE);
            populateArea();
        } else {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int DialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the account?", "Warning", dialogButton);
            if (DialogResult == JOptionPane.YES_OPTION) {
                Area area = (Area) tblArea.getValueAt(selectedRow, 0);
                for (Area a : district.getAreaDirectory().getAreaList()) {
                    if (a.equals(area)) {
                        district.getAreaDirectory().deleteArea(area);
                    }
                    populateArea();
                }
            }
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = tblArea.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row first", "Warning", JOptionPane.WARNING_MESSAGE);
            populateArea();
        } else {
            Area area = (Area) tblArea.getValueAt(selectedRow, 0);
            txtName.setEnabled(true);
            txtLx.setEnabled(true);
            txtLy.setEnabled(true);       
            txtName.setText(area.getName());
            txtLx.setText(String.valueOf(area.getLocation_x()));
            txtLy.setText(String.valueOf(area.getLocation_y()));
            txtType.setText(area.getClass().getTypeName());
      
            
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        int selectedRow = tblArea.getSelectedRow();
        Area area = (Area) tblArea.getValueAt(selectedRow, 0);
        area.setName(txtName.getText());
        area.setLocation_x(Double.valueOf(txtLx.getText()));
        area.setLocation_y(Double.valueOf(txtLy.getText()));
         JOptionPane.showMessageDialog(null, "Save Successfully", "Warning", JOptionPane.WARNING_MESSAGE);
         populateArea();
    
    }//GEN-LAST:event_btnSaveActionPerformed


