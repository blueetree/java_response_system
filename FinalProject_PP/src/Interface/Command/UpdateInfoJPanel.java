    public void populateArea() {
        DefaultTableModel dtm = (DefaultTableModel) tblArea.getModel();
        dtm.setRowCount(0);
        for (Area area : district.getAreaDirectory().getAreaList()) {
            Object row[] = new Object[3];
            row[0] = area;
            row[1] = area.getLocation_x();
            row[2] = area.getLocation_y();
          
            dtm.addRow(row);
            
        }
    }

