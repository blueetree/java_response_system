    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if (txtName.getText().length() < 1 || String.valueOf(txtPassword.getPassword()).length() < 1 || txtUserName.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Please complete blanks", "Error", ERROR_MESSAGE);
            return;
        }
        
        Country country = (Country) cbNetwork.getSelectedItem();
        String username = txtUserName.getText();
        String password = String.valueOf(txtPassword.getPassword());
        String Name = txtName.getText();

        Person person = country.getPersonDirectory().createPerson(Name);
       
        UserAccount account = country.getUserAccountDirectory().createUserAccount(username, password, person, new AdminCountryRole(),UserAccount.AccountType.Spot);
        System.out.println(country.getName()+" get(0):"+ country.getUserAccountDirectory().getUserAccountList().get(0));
       
        populateTable();
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblStateAdmin.getSelectedRow();
      
        if (selectedrow >= 0) {
            UserAccount ua = (UserAccount) tblStateAdmin.getValueAt(selectedrow, 0);
            Country e = (Country) tblStateAdmin.getValueAt(selectedrow, 2);
            e.getUserAccountDirectory().getUserAccountList().remove(ua);
            JOptionPane.showMessageDialog(null, "Deleted");
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row", "Warning", WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblStateAdmin.getSelectedRow();

        if (selectedrow >= 0) {
        UserAccount ua = (UserAccount) tblStateAdmin.getValueAt(selectedrow, 0);
       Country c = (Country) tblStateAdmin.getValueAt(selectedrow, 2);
        UpdateCountryAdminJPanel updateCountryAdminJPanel = new UpdateCountryAdminJPanel(c, ua, userProcessContainer);
        userProcessContainer.add("updateCountryAdminJPanel", updateCountryAdminJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a row", "Warning", WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


