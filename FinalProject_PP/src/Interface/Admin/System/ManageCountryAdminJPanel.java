    private void populateCountryCB() {
        cbNetwork.removeAllItems();
        for (Country country : system.getCountryList()) {
            cbNetwork.addItem(country);
        }
    }

    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblStateAdmin.getModel();

        model.setRowCount(0);
        for (Country country : system.getCountryList()) {
                for (UserAccount userAccount : country.getUserAccountDirectory().getUserAccountList()) {
                            Object[] row = new Object[4];
                            row[0] = userAccount;
                            row[1] = userAccount.getPerson();
                            row[2] = country;

                            model.addRow(row);
                        }
                    
                
        }
    }

