    private void populateNetworkTable() {
        DefaultTableModel model = (DefaultTableModel) tblNetwork.getModel();

        model.setRowCount(0);
        for (Country country : system.getCountryList()) {
            Object[] row = new Object[3];
            row[0] = country;
            row[1] = country.getLocation_x();
            row[2] = country.getLocation_y();
            model.addRow(row);
        }
    }

