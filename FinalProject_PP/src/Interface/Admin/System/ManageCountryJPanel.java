    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        String name = cbEType.getSelectedItem().toString();
        //System.out.println("txtLocationX.getText():"+txtLocationX.getText());
        //System.out.println(txtLocationX.getText().isEmpty());
        if (cbEType.getSelectedItem().toString()==null || txtLocationX.getText().isEmpty() ||txtLocationY.getText().isEmpty()) {
       
            JOptionPane.showMessageDialog(null, "Please Input Info", "Error", ERROR_MESSAGE);
            return;
        }else {
            for(Country country : system.getCountryList()){
                if(country.getName().equals(name)){
               JOptionPane.showMessageDialog(null, "Can not create the same country", "Error", ERROR_MESSAGE);
                 return; 
                }
            }
        }
        

        Country country = system.createAndAddCountry(name);
        //country.setName(name);
        double x = Double.parseDouble(txtLocationX.getText());
        double y = Double.parseDouble(txtLocationY.getText());
        country.setLocation_x(x);
        country.setLocation_y(y);
        populateNetworkTable();
    }//GEN-LAST:event_btnSubmitActionPerformed

