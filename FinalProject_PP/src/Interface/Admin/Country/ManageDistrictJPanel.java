    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        
        String name = txtName.getText();
        District.Type type = null;
        if(comboState.getSelectedItem().equals("Earthquake")){
            type = District.Type.Earthquake;
        }else if(comboState.getSelectedItem().equals("Mudflow")){
            type = District.Type.Mudflow;
        }
        
        if (country == null || name == null || type == null||txtLocationX.getText().isEmpty()|| txtLocationY.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }else {
            for(District district : country.getDistrictDirectory().getDistrictList()){
                if(district.getName().equals(name)){
               JOptionPane.showMessageDialog(null, "Can not create the same district", "Error", ERROR_MESSAGE);
                 return; 
                }
            }
        }
        
        District district = country.getDistrictDirectory().createDistrict(name,type);
        double x = Double.parseDouble(txtLocationX.getText());
        double y = Double.parseDouble(txtLocationY.getText());
        district.setLocation_x(x);
        district.setLocation_y(y);
        System.out.println(district.getName()+" :getcountry "+country.getName());
        populateTable();
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblState.getSelectedRow();
      
        if (selectedrow >= 0) {
            District d = (District) tblState.getValueAt(selectedrow, 0);
            Country c = (Country) tblState.getValueAt(selectedrow, 1);
            c.getDistrictDirectory().getDistrictList().remove(d);
            JOptionPane.showMessageDialog(null, "Deleted");
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row", "Warning", WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


