    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblState.getModel();
        model.setRowCount(0);

            for (District district : country.getDistrictDirectory().getDistrictList()) {
                Object[] row = new Object[3];
                row[0] = district;
                row[1] = district.getLocation_x();
                row[2] = district.getLocation_y();
                model.addRow(row);
            }
        
    }
    
