    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        // TODO add your handling code here:
        person.setFirstName(firstNameTextField.getText());
        person.setLastName(lastNameTextField.getText());
        person.setDob(dobTextField.getText());
        person.setGender(genderTextField.getText());
        person.setPhoto(photoTextField.getText());
        person.address.setCity(cityTextField.getText());
        person.address.setCountry(countryTextField.getText());
        person.address.setLine1(line1TextField.getText());
        person.address.setLine2(line2TextField.getText());
        person.address.setState(stateTextField.getText());
        person.address.setZipcode(zipcodeTextField.getText());
        person.creditCard.setName(creditNameTextField.getText());
        person.creditCard.setNum(Integer.parseInt(creditNumTextField.getText()));
        person.creditCard.setType(creditTypeTextField.getText());
        person.creditCard.setExpDate(creditExpDateTextField.getText());
        person.creditCard.setCvv(Integer.parseInt(creditCvvTextField.getText()));
        person.checkingAccount.setActive(checkingCheckBox.isSelected());
        person.checkingAccount.setCreationDate(checkingCreationDateTextField.getText());
        person.checkingAccount.setDebtOrCredit(checkingDebtOrCreditTextField.getText());
        person.checkingAccount.setExpDate(checkingExpDateTextField.getText());
        person.checkingAccount.setNum(Integer.parseInt(checkingNumTextField.getText()));
        person.savingAccount.setActive(savingCheckBox.isSelected());
        person.savingAccount.setCreationDate(savingCreationDateTextField.getText());
        person.savingAccount.setDebtOrCredit(savingDebtOrCreditTextField.getText());
        person.savingAccount.setExpDate(savingExpDateTextField.getText());
        person.savingAccount.setNum(Integer.parseInt(savingNumTextField.getText()));
        person.license.setEndDate(licenseEndDateTextField.getText());
        person.license.setId(Integer.parseInt(licenseIdTextField.getText()));
        person.license.setLevel(licenseLevelTextField.getText());
        person.license.setStartDate(licenseStartDateTextField.getText());
        person.license.setType(licenseTypeTextField.getText());
        
        JOptionPane.showMessageDialog(null, "Create Profile Successfully!");
          
    }//GEN-LAST:event_submitBtnActionPerformed

