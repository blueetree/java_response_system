    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        if(!txtPersonID.getText().isEmpty() && !txtFirstName.getText().isEmpty() && !txtLastName.getText().isEmpty()){
        Person person = personDirectory.addPerson();
        person.setPersonID(txtPersonID.getText());
        person.setFirstName(txtFirstName.getText());
        person.setLastName(txtLastName.getText());
        JOptionPane.showMessageDialog(null, "Create Successfully!");
        }else{
            JOptionPane.showMessageDialog(null, "Please input the correct info!");
        }
       
        
    }//GEN-LAST:event_btnCreateActionPerformed

