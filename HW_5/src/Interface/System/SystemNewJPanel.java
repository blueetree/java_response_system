    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        if(!txtUserPassword.getText().isEmpty() && !txtUserName.getText().isEmpty() && !txtPersonID.getText().isEmpty()){
             String personID = txtPersonID.getText();
        boolean create = true;
        for(Person person : personDirectory.getPersonDirectory()){
                if(person.getPersonID().equals(personID)){
                   UserAccount userAccount= person.getUserAccountDirectory().addUserAccount();
                   userAccount.setPassword(String.valueOf(hash(txtUserPassword.getText())));
                   userAccount.setStatus(rbActive.isSelected());
                   userAccount.setUserName(txtUserName.getText());
                   userAccount.setUserRole(cbUserRole.getSelectedItem().toString());
                   create = false;
                   JOptionPane.showMessageDialog(null, "Create Successfully!");
                }
            }
        if(create){
            Person person = personDirectory.addPerson();
            person.setPersonID(personID);
            UserAccount userAccount= person.getUserAccountDirectory().addUserAccount();
            userAccount.setPassword(String.valueOf(hash(txtUserPassword.getText())));
            userAccount.setStatus(rbActive.isSelected());
            userAccount.setUserName(txtUserName.getText());
            userAccount.setUserRole(cbUserRole.getSelectedItem().toString());
            JOptionPane.showMessageDialog(null, "Create Successfully!");
        }
        }else{
            JOptionPane.showMessageDialog(null, "Please input the correct info!");
        }
       
        
    }//GEN-LAST:event_btnCreateActionPerformed

