    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(!txtUserPassword.getText().isEmpty() && !txtUserName.getText().isEmpty()){

        userAccount.setPassword(String.valueOf(hash(txtUserPassword.getText())));
        userAccount.setStatus(rbActive.isSelected());
        userAccount.setUserName(txtUserName.getText());
        userAccount.setUserRole(cbUserRole.getSelectedItem().toString());
        JOptionPane.showMessageDialog(null, "Update Successfully!");
        
        txtUserName.setEditable(false);
        txtUserPassword.setEditable(false);
        cbUserRole.setEditable(false);
        rbActive.setEnabled(false);
        }else{
            JOptionPane.showMessageDialog(null, "Please input the correct info!");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        basePanel.remove(this);
        Component[] componentArray = basePanel.getComponents();
        Component component = componentArray[componentArray.length - 1];
        SystemTableJPanel panel = (SystemTableJPanel) component;
        panel.populateTable();
        CardLayout layout = (CardLayout)basePanel.getLayout();
        layout.previous(basePanel);
    }//GEN-LAST:event_btnBackActionPerformed


