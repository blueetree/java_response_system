    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String searchName = txtSearch.getText();
        UserAccount userAccount = new UserAccount();
        boolean result = true;
        for(Person person : personDirectory.getPersonDirectory()){
            UserAccountDirectory userAccountDirectory = person.getUserAccountDirectory();
            userAccount = userAccountDirectory.searchUserAccount(searchName);
         if(userAccount!=null){
            DefaultTableModel dtm = (DefaultTableModel)tblUserAccounts.getModel();
            dtm.setRowCount(0);
            Object row[] = new Object[3];
            row[0]=userAccount;
            row[1]=userAccount.getPassword();
            row[2]=userAccount.getUserRole();
            dtm.addRow(row);
            result = false;
            }
        }
         if(result){
              JOptionPane.showMessageDialog(null, "No user account found!");
         }
    }//GEN-LAST:event_btnSearchActionPerformed

