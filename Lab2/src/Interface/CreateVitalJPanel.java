    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        double temperature = Double.parseDouble(temTextField.getText());
        double bloodPressure = Double.parseDouble(bldTextField.getText());
        int pulse = Integer.parseInt(plsTextField.getText());
        String date = dateTextField.getText();
        VitalSigns v = vsh.addVitals();
        v.setBloodPressure(bloodPressure);
        v.setDate(date);
        v.setPulse(pulse);
        v.setTemperature(temperature);
        
        JOptionPane.showMessageDialog(null, "ok");
        
        temTextField.setText("");
        bldTextField.setText("");
        plsTextField.setText("");
        dateTextField.setText("");
        
    }//GEN-LAST:event_createBtnActionPerformed


