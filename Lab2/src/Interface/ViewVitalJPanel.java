    private void viewDtailsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDtailsBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = vitalSignsTbl.getSelectedRow();
        if (selectedRow >= 0){
            VitalSigns vs = (VitalSigns)vitalSignsTbl.getValueAt(selectedRow,0);
            temTextField.setText(String.valueOf(vs.getTemperature()));
            bldTextField.setText(String.valueOf(vs.getBloodPressure()));
            plsTextField.setText(String.valueOf(vs.getPulse()));
            dateTextField.setText(vs.getDate());
        }else
            JOptionPane.showMessageDialog(null, "Please select any row");
    }//GEN-LAST:event_viewDtailsBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = vitalSignsTbl.getSelectedRow();
        if (selectedRow >= 0){
            VitalSigns vs = (VitalSigns)vitalSignsTbl.getValueAt(selectedRow,0);
            vsh.deleteVitals(vs);
            JOptionPane.showMessageDialog(null, "ok");
            populateTable();
        }else
            JOptionPane.showMessageDialog(null, "Please select any row");
    }//GEN-LAST:event_deleteBtnActionPerformed


