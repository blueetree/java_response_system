    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    
   if(txtUserName.getText().equals("")||txtPassword.getText().equals(""))
             JOptionPane.showMessageDialog(null, "Don't leave blank!", "Info", JOptionPane.INFORMATION_MESSAGE);
    
     else{
     // ua.setAccountStatus();
        JOptionPane.showMessageDialog(null, "enter here again Updated", "Info", JOptionPane.INFORMATION_MESSAGE);
       if(jCheckBox2.isSelected())
        {
        ua.setAccountStatus(true);
        ua.setUserName(txtUserName.getText());
        if(!txtPassword.getText().equals("*********************"))
        {   String encoded =DatatypeConverter.printBase64Binary(txtPassword.getText().getBytes());
            ua.setPassWord(encoded);
        }
        JOptionPane.showMessageDialog(null, "Updated", "Info", JOptionPane.INFORMATION_MESSAGE);
    }
    
    else  if(jCheckBox1.isSelected())
    {
        ua.setAccountStatus(false);
        ua.setUserName(txtUserName.getText());
        if(!txtPassword.getText().equals("*********************"))
        {
            String encoded =DatatypeConverter.printBase64Binary(txtPassword.getText().getBytes());
            ua.setPassWord(encoded);
        }
        
        JOptionPane.showMessageDialog(null, "Updated", "Info", JOptionPane.INFORMATION_MESSAGE);
    }
    else
    {
         JOptionPane.showMessageDialog(null, "Please select a status", "Info", JOptionPane.INFORMATION_MESSAGE);
    }
      
     }// TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

