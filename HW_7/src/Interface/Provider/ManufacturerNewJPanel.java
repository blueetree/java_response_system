    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        if(!txtFirstName.getText().isEmpty()){
        Enterprise enterprise = network.getEnterpriseDirectory().AddEnterprise(txtFirstName.getText(), Enterprise.EnterpriseType.Manufacturer);
        enterprise.setEnterpriselocation(txtLocation.getText());
        Organization organization = enterprise.getOrganizationDirectory().addOrganization(Organization.Type.Provider);
        organization.setOrganizaitonName(txtOgName.getText());
        WorkArea workArea = organization.getWorkAreaDirectory().addWorkArea(txtWorkArea.getText());
        JOptionPane.showMessageDialog(null, "Create Successfully!");
        }else{
            JOptionPane.showMessageDialog(null, "Please input the correct info!");
        }
       
        
    }//GEN-LAST:event_btnCreateActionPerformed

